package ARRAYS;



public class OneD2 {
    public static void main(String [] args){


        int arr1[] = {1,2,3,4,5,67,87,109};
        for(int i=0;i<arr1.length;i++){
            System.out.print(arr1[i]+" ");
        }
        System.out.println("\n");

        int arr2[]={13,26,39,52,65,78,91,104,117,130};
        for(int num :arr2){
            System.out.print(num+" ");
        }
    }   
}
